                    <h4 class="my-3">What are the benefits of the TES?</h4>
                    <p class="text-muted">SUCs, LUCs LGU-run TVIs and TESDA TTIs:<br>

                        ● Allowance for books, supplies, transportation, and miscellaneous personal expenses, including
                        a reasonable allowance for the documented rental or purchase of personal computer or laptop, and
                        other education-related expenses;<br>
                        ● Allowance for room and board costs incurred by the student.
                        Private HEIs and TVIs:<br>

                        Students enrolled in private HEIs and TVIs are entitled to the following benefits (Sections 23
                        and 24. IRR of RA 10931):<br>

                        ● Tuition and other school fees, which shall be equivalent to the tuition and other school fees
                        of the nearest SUC and State-run TVI in their respective area.<br>
                        ● Allowance for books, supplies, transportation, and miscellaneous personal expenses, including
                        a reasonable allowance for the documented rental or purchase of personal computer or laptop, and
                        other education-related expenses<br>
