                    <h4 class="my-3">Who can avail of TES?</h4>
                    <p class="text-muted">He priority beneficiaries for the Tertiary Education Subsidy (TES) are:
                        <br>
                        ● Continuing ESGP-PA scholars in the SUCs effective AY 2018-2019;<br>
                        ● Students who are part of households included in the Listahanan 2.0, ranked according to the
                        estimated per capital household income; and<br>
                        ● students not part of the Listahanan 2.0, ranked according to estimated per capita household
                        income based on submitted documentation of proof of income to be determined by the UniFAST
                        Board;
                        Provided, further, that such prioritization shall not apply to Filipino students in cities and
                        municipalities with no existing SUC or LUC campus.
                    </p>
